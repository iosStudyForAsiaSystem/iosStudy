 ## 演習問題：コンソールアプリケーション
 
コンソールには、どのような種類のメッセージを送ることができますか？
*/
/*:
Finderで「アプリケーション」>「ユーティリティ」>「コンソール」を開くと、ほかのプログラマがMacに出力することにしたメッセージの一部を見ることができます。たくさんのテキストが表示されますが、ほとんど意味はわかりません。これらは、開発者の間の私的なメッセージで、各アプリケーションに詳しい人だけが理解できる仲間内の言葉が使われています。
*/
/*:
スクロールしていくつかのメッセージを見たら、コンソールアプリケーションを閉じてください。
*/
